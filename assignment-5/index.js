"use strict";
// This code block demonstrates how to store and display a famous person's quote.
// It stores the name of the famous person and the quote in variables.
// It then uses template literals to create a message that includes the famous person's name and their quote.
// Finally, it logs the message to the console.
// Store the famous person's name
let famous_person = "Albert Einstein";
// Store the famous person's quote
let quote = "A person who never made a mistake never tried anything new.";
// Use template literals to create a message that includes the famous person's name and their quote
let message = `${famous_person} once said,"${quote}"`;
// Log the message to the console
console.log(message);
console.log(message);
