"use strict";
// Function to make a shirt with a given size and message
// The size of the shirt
// The message to be printed on the shirt
function make_shirt(size, text) {
    // Print the details of the shirt being created
    console.log(`Creating A ${size} Shirt With The Message : ${text}`);
}
// Call the function with different sizes and messages
make_shirt("Small", "Big Dawg Things");
make_shirt("Medium", "Big Dawg Things");
make_shirt("Large", "Big Dawg Things");
